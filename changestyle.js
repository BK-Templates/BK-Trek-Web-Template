function ChangeColor(Color) {
if (Color == 'Enterprise') {
	document.getElementById("logo").style.background="url(images/l1.gif) no-repeat";
}
if (Color == 'Defiant') {
	document.getElementById("logo").style.background="url(images/l4.gif) no-repeat";
}
if (Color == 'Voyager') {
	document.getElementById("logo").style.background="url(images/l2.gif) no-repeat";
}
if (Color == 'Prometheus') {
	document.getElementById("logo").style.background="url(images/l3.gif) no-repeat";
}
}